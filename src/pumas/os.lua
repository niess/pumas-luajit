local jit = require('jit')

local os_ = {}


-------------------------------------------------------------------------------
-- Wrap the native os package
-------------------------------------------------------------------------------
for k, v in pairs(os) do
    os_[k] = v
end


-------------------------------------------------------------------------------
-- Add some OS specific constants
-------------------------------------------------------------------------------
if jit.os == 'Windows' then
    os_.PATHSEP = '\\'
    os_.LINESEP = '\r\n'
    os_.LIBEXT = 'dll'
else
    os_.PATHSEP = '/'
    os_.LINESEP = '\n'
    os_.LIBEXT = 'so'
end


-------------------------------------------------------------------------------
-- Return the sub-package
-------------------------------------------------------------------------------
return os_
